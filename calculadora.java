public class calculadora {
    static float somar(float a, float b){
        float c = a + b;
        return c;
    }
    static float subtracao(float a, float b){
        float c = a - b;
        return c;
    }
    static float multiplicacao(float a, float b){
        float c = a*b;
        return c;
    }
    static float divisao(float a, float b){
        float c = a/b;
        return c;
    }
}

